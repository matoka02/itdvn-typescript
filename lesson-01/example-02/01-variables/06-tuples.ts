// 6. Tuples
// Кортеж - упорядоченный набор фиксированной длины
let x : [string, number];
x = ['hello', 10];

console.log(x);       // ['hello', 10]
console.log(x[0]);    // hello
console.log(x[1]);    // 10

const y: [number, string, string] = [1, 'hello', 'world'];
console.log(y[2]);    // 'world'

